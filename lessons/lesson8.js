// Functions

// Declarative Function
function helloOne(){
    console.log('Hello Marcus!')
}

// Anonymous Function
var helloTwo = function(){
    console.log('Hello Bruno!')
}

// ES6 Function Syntax or Arrow Function
var helloThree = () => {
    console.log('Hello Rasmus!')
}

// Invoking the functions
helloOne()
helloTwo()
helloThree()