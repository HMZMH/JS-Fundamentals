// Relational Operators

// > - More Than
// < - Less Than
// >= More Than Equal
// <= Less Than Equal

var result1 = 3 > 2
var result2 = 3 < 2
var result3 = 2 >= 2
var result4 = 3 <= 2

console.log(result1)
console.log(result2)
console.log(result3)
console.log(result4)

// Equality Operators

var x = 1
console.log(x == '1') // Loose Comparison
console.log(x === '1') // Strict Comparison