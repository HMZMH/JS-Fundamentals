// Relational Operators

// > - More Than
// < - Less Than
// >= More Than Equal
// <= Less Than Equal

var result1 = 10 > 5
var result2 = 10 < 5
var result3 = 5 >= 5
var result4 = 6 <= 5

console.log(result1)
console.log(result2)
console.log(result3)
console.log(result4)

// Equality Operators

var x = 1
console.log(x == '1') // Loose Comparison
console.log(x === '1') // Strict Comparison